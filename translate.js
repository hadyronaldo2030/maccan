function changeLanguage(lang) {
    i18next.changeLanguage(lang, function (err, t) {
        localStorage.setItem('language', lang);
        updateContent();
    });
}

function updateContent() {
    // تحديث الترجمة على ال navbar
    document.querySelectorAll('[data-i18n]').forEach(element => {
        element.innerHTML = i18next.t(element.getAttribute('data-i18n'));
    });

    // تحديث اتجاه العناصر HTML الأخرى
    var direction = (i18next.language === 'ar') ? 'rtl' : 'ltr';
    document.getElementById('direction').dir = direction;

    // إظهار أو إخفاء العناصر بناءً على اللغة المحددة
    var isArabic = (i18next.language === 'ar');
    document.getElementById('englishText').style.display = isArabic ? 'none' : 'block';
    document.getElementById('arabicText').style.display = isArabic ? 'block' : 'none';

    // اعطاء margin الى عناصر ال features
    var elements = document.getElementsByClassName('margin-3');
    for (var i = 0; i < elements.length; i++) {
        elements[i].style.margin = isArabic ? '8px 0 0 15px' : '8px 15px 0 0';
    }
                                                        
}

$(document).ready(function () {
    // استدعاء الدالة عند تحميل الصفحة
    updateContent();
});


document.addEventListener('DOMContentLoaded', updateContent);